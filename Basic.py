# Print A Set
Fruits = {"Apple", "Orange", "Mango", "Banana"}
print("Initial Set :", Fruits)

# Add Element in set
Fruits.add("Grapes")
print("Set after add Element :", Fruits)

# Remove Element from set
Fruits.remove("Banana")
print("Set after Remove Element 'Banana' :", Fruits)

# Discard Element From Set
Fruits.discard("Mango")
print("Set after Discard Element 'Mango' :", Fruits)
